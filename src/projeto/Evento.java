package Projeto;

public class Evento {
    private String data;
    private String local;
    private String compraIngresso;
    private String nomeOrganizador;

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getLocal() {
        return local;
    }

    public void setLocal(String local) {
        this.local = local;
    }

    public String getCompraIngresso() {
        return compraIngresso;
    }

    public void setCompraIngresso(String compraIngresso) {
        this.compraIngresso = compraIngresso;
    }

    public String getNomeOrganizador() {
        return nomeOrganizador;
    }

    public void setNomeOrganizador(String nomeOrganizador) {
        this.nomeOrganizador = nomeOrganizador;
    }
    
}